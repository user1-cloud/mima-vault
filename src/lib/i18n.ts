export type Locale = "zh" | "en";

const locales: Record<Locale, typeof zh> = { zh, en };

let current: Locale = (localStorage.getItem("mima-lang") as Locale) ?? "zh";

export function setLocale(locale: Locale) {
  current = locale;
  localStorage.setItem("mima-lang", locale);
}

export function getLocale(): Locale {
  return current;
}

export function t(key: keyof typeof zh, vars?: Record<string, string | number>): string {
  let text: string = locales[current][key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}
